              <h3>Triprayar Mele Thrikkovil Mahadeva Temple</h3>
              <p><blockquote>near Sree Rama Polytechnic Triprayar. The grand shrines like Guruvaayoor Sri Krishna Swamy Temple, Vadakkunnathan Temple,Koodalmaanikyam, Kodungalloor etc are measured under the perception of Bhoolokavaikundam (The seat of God in Earth).Equivalent distance form these places, Melthrikkovil Siva Temple is located, as the domicile of tranquility, opulence and divinity. This temple which offers supreme opulence is very close to the Thriprayar Sri Rama Swamy Temple. This place is considered as divine as, Great Brahmins were born in this place. This temple holds historical significance in view of the fact that the devotees are worshiping for several years. Tracing the history, this shrine was one among the preliminary points of Siva reverence initiated by the Dravidians which prevailed before the Vaishnava supremacy.</p>
            <img src="img/triprayarmelthrikkovil.jpg" class="img-responsive">
            <p>History says that the Thriprayar Sri Rama Swamy temple was under decree of Melthrikkovil, which in turn was under the Blaha Mooppil Nair till 1016 and then it was given to the Maharaja of Kochi. Same as that of Guruvaayoor Sri Krishna Swamy temple and Mammiyoor Siva temple, The Thriprayar Sri Rama Swamy temple and Melthrikkovil Siva temple are interdependently associated. These types of temples are worshipped as ‘Kudumbaparadevata” form by the earliest Brahmin families. Due to generation’s unawareness, all such temples faced absolute abandoness and Melthrikkovil Siva temple came into the fortification of Blaha Tharavaadu. At that time this region was ruled by the British dominion. During this period, Aarattu, Usavam, Sivarathrifestival, Thriuvaathira, Onam, Vishu, Balitharppanam, Pradosha poojas, etc were celebrated in the temple. When years passed the Blaha Tharavaadu starts to get obliterate and temple rituals and festivals began to diminish for many years. This resulted in the commencement of many fears and disasters such as epidemics, abnormal deaths etc in the area. To get divest these troubles, the control of the temple was transferred to Mele Thrikkovil Kshethra samithi by the then Karanavar of Blaha Tharavadu, Sri Kochunni Nair in 1994. The idol here is the rarest of all Mahadeva, accompanied by Sri Parvathy form, which gives the Darshana to east ward direction. The Thanthric Sthaanam ( the authority to do the Thanthric ceremonies) in this temple is given to the well-known Anthikkadu Pazhangaaparambu Mana. The Upadevathas here are Mahaganapathi, Subrahmanyan, and Damabathi Shasthavu. Besides this, there is the very old Nagas (serpent Gods) and Naga Kavu (Sacred Grooves) can be seen in the Chuttambalam which is positioned by doing special Poojas and rituals (Aavahanas). The Sarppabali which is done in the month of Vrischika (Uthram Nakshthrara) is considered superior for Santhanalabthi (obtaining good child) and also as a remedy for several illness. The Damabathi Shasthavu who is a unique Prathishta (deity) can be spotted here only. Paying homage to this god, well and prosper marriage life is accomplished. Thiruvaathira in Dhanu month, Krishnapaksha Chathurdashi or Shivarathhry in Kumbha month, Siva Chadurdashi, Pradosha Pooja, Vaavu Bali etc are the chief festivals and key dates of temple. The rarest “Golaka” of Sri Mahadeva (which can’t be seen even in North Indian temples , the Valampiri Shanghu adorned with gold and Navarathna are yet another peculiarity of Melthrikkovil Temple.</blockquote></p>
            <h3>Triprayar Shanmukha Samajam Subrahmanya Hanuman Temple</h3>
            <blockquote>Triprayar Shanmukha Samajam Subrahmanya Hanuman Temple is located close to the western gopura of Rama Temple. Subrahmanya and Hanuman are the main deities of this temple. The idol of Hanuman is 18 feet long and one of the biggest Hanuman idol in Kerala. The temple is managed by Triprayar Shanmukha Samajam</blockquote>
        <h3>Erakulangara Shiva Temple</h3>
        <blockquote>Erakulangara Shiva Temple is located at Triprayar Kizhakke Nada(east nada of Triprayar Temple). The presiding deity is Lord Shiva(facing east). Ganapathy, Naga Devathas, Navagrahas and Bhagavathi(Bhadrakali) are the upadevathas. Temple pond or Erakulangara chira is located infront of the temple. Erakulangara Shiva Temple is managed by Cochin Devaswom Board, Pazhuvam Devaswom.</blockquote>
