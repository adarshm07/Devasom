              <blockquote>
                <h3>Pooja Timing</h3>

                  <p>The temple opens at 3 a. m in the morning and remains open till 12 p.m. The temple reopens at 4.30 p.m in the evening and remains open till 8.30 p. m.</p></blockquote>                 
                  <blockquote> There are five poojas conducted here as in all great temples- usha pooja, ethirtha pooja, panthiradi pooja, ucha pooja and athazha pooja. There are three sheevelis (a processional image of the deity is carried around the temple) - usha sheeveli, ucha sheeveli and athazha sheeveli. For Ucha sheeveli, elephents are not used for procession. In Mandalakalam (November-December) there will be a Kazcha Sheeveli instead of Ucha sheeveli.</blockquote>
                  
                  <blockquote> Nirmalya darshan (early morning when the sanctum opens for the day) and athazha pooja are considered very sacred. For athazha pooja especially devas and rishis are supposed to be present to have darshan of the Lord.</p></blockquote> 
                  
