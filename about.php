        <h1 class="heading">TRIPRAYAR SHREE RAMASWAMI TEMPLE</h1>
        <p><strong>Shree Ramaswami Temple
          Temple entrance seen from bank of river Theevra
          Temple entrance seen from bank of river Theevra
          Geography
          Location	Triprayar
          Country	India
          State	Kerala
          District	Thrissur District
          Culture
          Sanctum	Rama
          Major festivals	Arattupuzha Pooram, Ekadasi, Sethubandhanam at Sreeraman Chira Chemmappilly
          Architecture
          Architecture	Kerala
          Thriprayar Shree Ramaswami Temple is Hindu temple situated in Triprayar in Thrissur district of Kerala state in India. The deity is Lord Rama, the seventh incarnation of Lord Vishnu, with four arms bearing a conch, a disc, a bow and a garland. The temple is situated on the bank of river Theevra. The temple deity is the presiding deity of Arattupuzha Pooram. It is believed that the idol here was worshipped by Lord Krishna, another avatar of Lord Vishnu in Dwarka. Along with Lord Rama, there are shrines for Lord Shiva as Dakshinamoorthy, Lord Ganesha, Lord Sastha and Lord Krishna.</strong></p>
