        <h2> Nalambalam Yatra</h2>
        <p class="lead">
Nalambalam Yatra begins from the Rama temple in Triprayar and ends up at S hatrughna Temple in Payyammal. It is customary to visit the four temples in the order Rama, Bharata, Lakshmana and Shatrughna respectively. A pilgrimage to these temples on a single day during the holy month of Karkkidakam believed to shower blessings and ensure prosperity. Starting from Thripprayar and ending in Payammel, through Irinjalakuda and Moozhikulam.</p>
        <p>A trip to Nalambalam is a highly sacred auspicious event during Karkidakam, a highly sacred month in the Malayalam calender. Nalambalam is one of the eminent pilgrimage sites of South India that consists of the shrines of Lord Rama, Lakshmana, Bharata and Shatrughna, the celebrated quartet of the epic Ramayana.</p>
        <p><a class="btn btn-ghost">History</a></p>
        <p>Nalanbalam Yathra is a unique and novel pilgrimage conducted during the month of Karkitakam, the last month of Malayalam Era. Salient feature of Karkitaka is that it is period of famine and illness. To overcome sufferings people resort to medical treatment and spiritual enrichment. A special way of living and worship of Rama is the general trend of he period. Chanting of Rama mantra and Ramayana recitation echoes in each and every house . This is an eve of positive thinking about prosperity.

Since the last decade Nalambalam Yathra has become in Vogue. It is a special kind of visiting 4 temples in Kerala – Sreerama Temple at Triprayar, Koodalmanickam Bharatha temple at Irinjalakuda,Lakshmana temple at Moozhikkulam in Ernkulam District and Payammel Sathrukhna temple near Irinjalakuda.

Triprayar is about 23 kms away from Guruvayoor Krishna temple and Moozhikulam is 30 kms away from Irinjalakuda temple via Mala, Annamanada to Aluva. Visiting four temples a day in the given order is the traditional way.</p>
